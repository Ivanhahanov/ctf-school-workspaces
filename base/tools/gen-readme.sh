#!/bin/bash
# Regenerate README.md from a built Docker image.
# Usage: ./tools/gen-readme.sh [image:tag]
set -e
cd "$(dirname "$0")/.."

IMAGE="${1:-ctf-desktop:latest}"
echo "Collecting package data from $IMAGE ..."

docker run --rm --entrypoint bash "$IMAGE" \
    -c "dpkg-query -W -f='\${Package}|\${Version}|\${Installed-Size}\n'" \
    | sort > /tmp/ctf-pkgs.txt

SIZE=$(docker image inspect "$IMAGE" --format '{{.Size}}' \
    | awk '{printf "%.0f MB", $1/1024/1024}')
CREATED=$(docker image inspect "$IMAGE" --format '{{.Created}}' | cut -dT -f1)
PKG_COUNT=$(wc -l < /tmp/ctf-pkgs.txt)

python3 - "$IMAGE" "$SIZE" "$CREATED" "$PKG_COUNT" << 'EOF'
import sys

image, size, created, pkg_count = sys.argv[1:]

pkgs = []
with open('/tmp/ctf-pkgs.txt') as f:
    for line in f:
        parts = line.strip().split('|')
        if len(parts) == 3:
            name, version, size_kb = parts
            pkgs.append(f"| `{name}` | {version} | {size_kb} KB |")

pkg_table = "\n".join(pkgs)

readme = f"""# CTF Desktop — Base Image

> Browser-based Linux desktop for CTF competitions. Each participant gets their
> own isolated container accessible via a browser (noVNC) — no client installation needed.
> The CTF user has **no root access**. Flags are injected at container start via `FLAG` env.

## Architecture

```
ctf-desktop:latest          ← this image (base)
├── Dockerfile.pentest      ← + nmap, gdb, pwntools, radare2
└── Dockerfile.programming  ← + python3, gcc, node, java
```

## Quick start

```bash
# Build
docker build -t ctf-desktop:latest .
docker build -t ctf-desktop-pentest:latest -f Dockerfile.pentest .

# Run (one container per participant)
docker run -d -p 6080:6080 --shm-size=512m \\\\
  -e CTF_USER=alice \\\\
  -e CTF_HOSTNAME=pentest-01 \\\\
  -e FLAG='CTF{{your_flag_here}}' \\\\
  ctf-desktop-pentest:latest

# Browser → http://HOST:6080/
```

## Environment variables

| Variable | Default | Description |
|----------|---------|-------------|
| `CTF_USER` | `ctf` | Linux username inside container |
| `CTF_HOSTNAME` | `ctf-box` | Hostname shown in shell prompt |
| `FLAG` | _(none)_ | Written to `/flag` (chmod 400, root-only) at startup |
| `GEOMETRY` | `1280x800` | VNC resolution |

## Access

Open **`http://HOST:PORT/`** — auto-connects and scales to browser window.
For true fullscreen press **F11**. VNC has no password — access is
controlled at the network/port level (one container per user).

## Security model

```
/flag   →   -r-------- root root   (CTF user cannot read)
sudo    →   not installed
```

The flag is never baked into the image — passed at `docker run` time.

## Base image contents

> **Image:** `{image}`  **Size:** {size}  **Built:** {created}

### Desktop

| Component | Details |
|-----------|---------|
| Window manager | XFCE4 + Arc-Dark theme |
| Terminal | xfce4-terminal (green-on-black, Hack font) |
| File manager | Thunar |
| Browser | Firefox ESR |
| Remote access | TigerVNC + noVNC (WebSocket, auto-connect) |
| Wallpaper | Dark green grid (`tools/gen-wallpaper.py`) |

### Included CLI tools

| Tool | Purpose |
|------|---------|
| `curl`, `wget` | HTTP |
| `netcat` (openbsd) | TCP/UDP |
| `file`, `xxd` | File inspection / hex dump |
| `vim` | Editor |
| `tmux` | Terminal multiplexer |

### Not in base — add via child images

| Tool | Child image |
|------|-------------|
| python3, pip | `Dockerfile.programming` |
| nmap, gdb, pwntools | `Dockerfile.pentest` |
| gcc, node, java | `Dockerfile.programming` |
| radare2, tcpdump | `Dockerfile.pentest` |

## Full package list ({pkg_count} packages)

<details>
<summary>Expand</summary>

| Package | Version | Installed size |
|---------|---------|----------------|
{pkg_table}

</details>

---
*Auto-generated by `tools/gen-readme.sh {image}`*
"""

with open('README.md', 'w') as f:
    f.write(readme)
print(f"README.md written ({pkg_count} packages)")
EOF
